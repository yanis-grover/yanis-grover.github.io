// Initialize GSAP animations after DOM content loaded
document.addEventListener('DOMContentLoaded', () => {
  // Check if main content elements exist before animating
  const mainContentElements = document.querySelectorAll('main > h1, main > p, #get-in-touch');
  if (mainContentElements.length === 0) {
    console.warn('Main content elements not found. Skipping animations.');
    return;
  }

  // Create sequential fade-in animations for all main content elements
  const animationTimeline = gsap.timeline();
  mainContentElements.forEach((element, index) => {
    animationTimeline.to(element, {
      y: -20,
      opacity: 1,
      duration: 0.5,
      ease: 'power2.inOut',
      delay: index * 0.2
    });
  });
});